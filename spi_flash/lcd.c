	buffer[0] = 0x66;
	writeCycle(0x3A, buffer, 1); 	// Pixel Format 18-bpp

	buffer[0] = 0x1F;
	buffer[1] = 0x1B;
	buffer[2] = 0x18;
	buffer[3] = 0x0B;
